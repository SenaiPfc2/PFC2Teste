package atributos;


public class Aparelhos {
    String aparelho;
    String marca, so, queixa;
    int id_aparelho;

    /**
     * @return the aparelho
     */
    public String getAparelho() {
        return aparelho;
    }

    /**
     * @param aparelho the aparelho to set
     */
    public void setAparelho(String aparelho) {
        this.aparelho = aparelho;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the so
     */
    public String getSo() {
        return so;
    }

    /**
     * @param so the so to set
     */
    public void setSo(String so) {
        this.so = so;
    }

    /**
     * @return the queixa
     */
    public String getQueixa() {
        return queixa;
    }

    /**
     * @param queixa the queixa to set
     */
    public void setQueixa(String queixa) {
        this.queixa = queixa;
    }

    /**
     * @return the id_aparelho
     */
    public int getId_aparelho() {
        return id_aparelho;
    }

    /**
     * @param id_aparelho the id_aparelho to set
     */
    public void setId_aparelho(int id_aparelho) {
        this.id_aparelho = id_aparelho;
    }
}
